    private void btnfindSVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnfindSVActionPerformed
        // TODO add your handling code here:
        if (txtfindMaSV.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ban chua nhap ma sinh vien");
        } else {
            Sinhvien sv = svDao.getSinhVienByID(txtfindMaSV.getText());
            if (sv != null) {
                Grade d = dDao.getOneGradebyMaSV(sv.getMaSV());
                if (d != null) {
                    setModel(d);
                } else {
                    txtMasv.setText(sv.getMaSV());
                    txtHoten.setText(sv.getTenSV());
                    txtToan.setText("-1");
                    txtly.setText("-1");
                    txthoa.setText("-1");
                }
            }
        }
    }//GEN-LAST:event_btnfindSVActionPerformed

