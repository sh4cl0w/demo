    private void btnfix1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnfix1ActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            Grade d = Getmodel();
            if (dDao.updateGrade(d) > 0) {
                JOptionPane.showMessageDialog(this, "Cap nhat thanh cong");
                fillDataTABLE();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ban chua nhap thong tin");
        }


    }//GEN-LAST:event_btnfix1ActionPerformed

    public void fillDataTABLE() {
        DefaultTableModel model = (DefaultTableModel) tbsinhvien.getModel();
        model.setRowCount(0);
        for (Grade g : dDao.getAllGrade()) {
            Object rowData[] = new Object[7];
            rowData[0] = g.getSv().getMaSV();
            rowData[1] = g.getSv().getTenSV();
            rowData[2] = g.getToan();
            rowData[3] = g.getLy();
            rowData[4] = g.getHoa();
            rowData[5] = g.getTBC();
            rowData[6] = g.getXeploai();
            model.addRow(rowData);
        }

    }

    public boolean validateForm() {
        if (txtMasv.getText().isEmpty()
                || txtHoten.getText().isEmpty()
                || txtToan.getText().isEmpty()
                || txtly.getText().isEmpty()
                || txthoa.getText().isEmpty()) {
            return false;
        }
        return true;
    }

    public Grade Getmodel() {
        Grade d = new Grade();
        d.setId(0);
        Sinhvien sv = new Sinhvien(txtMasv.getText(), txtHoten.getText());
        d.setSv(sv);
        d.setToan(Double.parseDouble(txtToan.getText()));
        d.setLy(Double.parseDouble(txtly.getText()));
        d.setHoa(Double.parseDouble(txthoa.getText()));
        return d;

    }
    private void btnsave1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsave1ActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            Grade g = Getmodel();
            if (dDao.getOneGradebyMaSV(g.getSv().getMaSV()) != null) {
                JOptionPane.showMessageDialog(this, "ma sinh vien nay da ton tai");
            } else {

                if (dDao.add(g) > 0) {
                    JOptionPane.showMessageDialog(this, "Them thanh cong");
                    fillDataTABLE();
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Xin vui long kiem tra thong tin");

        }

    }//GEN-LAST:event_btnsave1ActionPerformed

